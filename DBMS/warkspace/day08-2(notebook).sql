CREATE TABLE PET(
	PIN VARCHAR2(300) PRIMARY KEY,
	NAME VARCHAR2(200) NOT NULL,
	AGE NUMBER(5) DEFAULT 1,
	OWERID VARchar2(200),
	CONSTRAINT PET_FK FOREIGN KEY(OWERID) REFERENCES OWER(ID);
	
);

